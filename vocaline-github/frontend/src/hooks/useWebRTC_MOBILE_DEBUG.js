import { useState, useRef, useCallback, useEffect } from 'react';
import { useSocket } from './useSocket';

export const useWebRTC = () => {
  const [isConnected, setIsConnected] = useState(false);
  const [remoteStream, setRemoteStream] = useState(null);
  const [localStream, setLocalStream] = useState(null);
  const [isMuted, setIsMuted] = useState(false);
  const [connectionState, setConnectionState] = useState('new');
  
  const peerConnection = useRef(null);
  const { socket } = useSocket();
  
  // ðŸ“± LOGS MOBILES RENFORCÃ‰S
  const logMobileDebug = useCallback((message, data = {}) => {
    const timestamp = new Date().toISOString();
    const userAgent = navigator.userAgent;
    const isMobile = /Android|iPhone|iPad|iPod|BlackBerry|IEMobile|Opera Mini/i.test(userAgent);
    
    console.log(`ðŸ“± MOBILE DEBUG [${timestamp}]: ${message}`, {
      ...data,
      isMobile,
      userAgent: userAgent.substring(0, 100),
      connection: navigator.connection?.effectiveType || 'unknown',
      online: navigator.onLine
    });
    
    // Envoyer au serveur pour logs
    if (socket) {
      socket.emit('mobile_debug', {
        type: 'mobile_debug',
        message,
        data: {
          ...data,
          isMobile,
          userAgent,
          connection: navigator.connection?.effectiveType || 'unknown',
          online: navigator.onLine,
          timestamp
        }
      });
    }
  }, [socket]);

  // Configuration WebRTC avec logs renforcÃ©s
  const createPeerConnection = useCallback(() => {
    logMobileDebug('CrÃ©ation PeerConnection', { state: 'initializing' });
    
    const configuration = {
      iceServers: [
        { urls: 'stun:stun.l.google.com:19302' },
        { urls: 'stun:stun1.l.google.com:19302' }
      ]
    };
    
    const pc = new RTCPeerConnection(configuration);
    
    // Logs d'Ã©tat de connexion
    pc.onconnectionstatechange = () => {
      const state = pc.connectionState;
      logMobileDebug(`Ã‰tat connexion WebRTC: ${state}`, { connectionState: state });
      setConnectionState(state);
      
      if (socket) {
        socket.emit('webrtc_state', {
          type: 'connection_state',
          state,
          debug: `MOBILE_CONNECTION_${state.toUpperCase()}`
        });
      }
    };
    
    // Logs ICE
    pc.oniceconnectionstatechange = () => {
      const iceState = pc.iceConnectionState;
      logMobileDebug(`Ã‰tat ICE: ${iceState}`, { iceConnectionState: iceState });
      
      if (socket) {
        socket.emit('webrtc_state', {
          type: 'ice_state',
          state: iceState,
          debug: `MOBILE_ICE_${iceState.toUpperCase()}`
        });
      }
    };
    
    // Candidats ICE avec logs
    pc.onicecandidate = (event) => {
      if (event.candidate) {
        logMobileDebug('Candidat ICE gÃ©nÃ©rÃ©', { 
          candidate: event.candidate.candidate.substring(0, 50) + '...',
          type: event.candidate.type
        });
        
        if (socket) {
          socket.emit('ice-candidate', event.candidate);
          socket.emit('webrtc_ice', {
            type: 'ice_candidate_sent',
            candidateType: event.candidate.type,
            debug: 'MOBILE_ICE_CANDIDATE_SENT'
          });
        }
      } else {
        logMobileDebug('Fin des candidats ICE');
      }
    };
    
    // Stream distant avec logs renforcÃ©s
    pc.ontrack = (event) => {
      logMobileDebug('Stream distant reÃ§u', { 
        streamId: event.streams[0]?.id,
        trackCount: event.streams[0]?.getTracks().length,
        audioTracks: event.streams[0]?.getAudioTracks().length
      });
      
      const stream = event.streams[0];
      setRemoteStream(stream);
      
      // Logs audio spÃ©cifiques
      const audioTracks = stream.getAudioTracks();
      if (audioTracks.length > 0) {
        const audioTrack = audioTracks[0];
        logMobileDebug('Track audio dÃ©tectÃ©', {
          enabled: audioTrack.enabled,
          muted: audioTrack.muted,
          readyState: audioTrack.readyState,
          settings: audioTrack.getSettings()
        });
        
        // Tentative de lecture automatique avec logs
        const audio = new Audio();
        audio.srcObject = stream;
        audio.autoplay = true;
        audio.playsInline = true;
        
        audio.play().then(() => {
          logMobileDebug('Autoplay rÃ©ussi', { success: true });
          
          if (socket) {
            socket.emit('webrtc_audio_state', {
              type: 'autoplay_success',
              enabled: true,
              debug: 'MOBILE_AUTOPLAY_SUCCESS'
            });
          }
        }).catch((error) => {
          logMobileDebug('Autoplay bloquÃ©', { 
            error: error.message,
            name: error.name
          });
          
          if (socket) {
            socket.emit('webrtc_audio_state', {
              type: 'autoplay_blocked',
              enabled: false,
              error: error.message,
              debug: 'MOBILE_AUTOPLAY_BLOCKED'
            });
          }
        });
      }
      
      if (socket) {
        socket.emit('webrtc_audio_state', {
          type: 'remote_received',
          enabled: true,
          streamId: stream.id,
          audioTracks: audioTracks.length,
          debug: 'MOBILE_STREAM_RECEIVED'
        });
      }
    };
    
    return pc;
  }, [socket, logMobileDebug]);

  // DÃ©marrage d'appel avec logs mobiles
  const startCall = useCallback(async (stream) => {
    logMobileDebug('DÃ©marrage appel WebRTC', { hasStream: !!stream });
    
    try {
      if (!peerConnection.current) {
        peerConnection.current = createPeerConnection();
      }
      
      const pc = peerConnection.current;
      
      // Ajout du stream local avec logs
      if (stream) {
        const audioTracks = stream.getAudioTracks();
        logMobileDebug('Ajout stream local', {
          streamId: stream.id,
          audioTracks: audioTracks.length,
          trackSettings: audioTracks[0]?.getSettings()
        });
        
        stream.getTracks().forEach(track => {
          pc.addTrack(track, stream);
        });
        setLocalStream(stream);
      }
      
      // CrÃ©ation offre avec logs
      logMobileDebug('CrÃ©ation offre WebRTC');
      const offer = await pc.createOffer({
        offerToReceiveAudio: true,
        offerToReceiveVideo: false
      });
      
      logMobileDebug('Offre crÃ©Ã©e', { 
        type: offer.type,
        sdpLength: offer.sdp.length
      });
      
      await pc.setLocalDescription(offer);
      logMobileDebug('Description locale dÃ©finie');
      
      if (socket) {
        socket.emit('offer', offer);
        socket.emit('webrtc_offer', {
          type: 'offer_sent',
          offerType: offer.type,
          debug: 'MOBILE_OFFER_SENT'
        });
      }
      
      setIsConnected(true);
      
    } catch (error) {
      logMobileDebug('Erreur dÃ©marrage appel', {
        error: error.message,
        name: error.name,
        stack: error.stack?.substring(0, 200)
      });
      
      if (socket) {
        socket.emit('webrtc_error', {
          type: 'start_call_error',
          error: error.message,
          debug: 'MOBILE_START_CALL_ERROR'
        });
      }
    }
  }, [createPeerConnection, socket, logMobileDebug]);

  // Fonction pour dÃ©marrer manuellement l'audio (mobile)
  const startRemoteAudio = useCallback(async () => {
    logMobileDebug('DÃ©marrage manuel audio distant');
    
    if (remoteStream) {
      try {
        // Activation AudioContext pour mobile
        if (window.AudioContext || window.webkitAudioContext) {
          const AudioContext = window.AudioContext || window.webkitAudioContext;
          const audioContext = new AudioContext();
          
          if (audioContext.state === 'suspended') {
            await audioContext.resume();
            logMobileDebug('AudioContext activÃ©', { state: audioContext.state });
          }
        }
        
        // Lecture forcÃ©e
        const audio = new Audio();
        audio.srcObject = remoteStream;
        audio.autoplay = true;
        audio.playsInline = true;
        
        await audio.play();
        
        logMobileDebug('Audio distant dÃ©marrÃ© manuellement', { success: true });
        
        if (socket) {
          socket.emit('webrtc_audio_state', {
            type: 'remote_manual_start',
            enabled: true,
            debug: 'MOBILE_MANUAL_SUCCESS'
          });
        }
        
        return true;
      } catch (error) {
        logMobileDebug('Erreur dÃ©marrage manuel audio', {
          error: error.message,
          name: error.name
        });
        
        if (socket) {
          socket.emit('webrtc_audio_state', {
            type: 'remote_manual_error',
            enabled: false,
            error: error.message,
            debug: 'MOBILE_MANUAL_ERROR'
          });
        }
        
        return false;
      }
    }
    
    return false;
  }, [remoteStream, socket, logMobileDebug]);

  // Gestion des Ã©vÃ©nements socket avec logs
  useEffect(() => {
    if (!socket) return;
    
    const handleOffer = async (offer) => {
      logMobileDebug('Offre reÃ§ue', { type: offer.type });
      
      try {
        if (!peerConnection.current) {
          peerConnection.current = createPeerConnection();
        }
        
        const pc = peerConnection.current;
        
        // CORRECTION CRITIQUE : Le rÃ©cepteur doit aussi ajouter son stream audio
        const audioStream = window.vocalineAudioStream;
        if (audioStream && audioStream.active) {
          logMobileDebug('Ajout stream local (rÃ©cepteur)', {
            streamId: audioStream.id,
            audioTracks: audioStream.getAudioTracks().length
          });
          
          audioStream.getTracks().forEach(track => {
            pc.addTrack(track, audioStream);
          });
          setLocalStream(audioStream);
        } else {
          logMobileDebug('ERREUR: Aucun stream audio pour le rÃ©cepteur', { 
            hasGlobalStream: !!window.vocalineAudioStream 
          });
        }
        
        await pc.setRemoteDescription(offer);
        
        logMobileDebug('Description distante dÃ©finie');
        
        const answer = await pc.createAnswer();
        await pc.setLocalDescription(answer);
        
        logMobileDebug('RÃ©ponse crÃ©Ã©e et dÃ©finie', { type: answer.type });
        
        socket.emit('answer', answer);
        socket.emit('webrtc_answer', {
          type: 'answer_sent',
          answerType: answer.type,
          debug: 'MOBILE_ANSWER_SENT'
        });
        
      } catch (error) {
        logMobileDebug('Erreur traitement offre', {
          error: error.message,
          name: error.name
        });
        
        socket.emit('webrtc_error', {
          type: 'offer_handling_error',
          error: error.message,
          debug: 'MOBILE_OFFER_ERROR'
        });
      }
    };
    
    const handleAnswer = async (answer) => {
      logMobileDebug('RÃ©ponse reÃ§ue', { type: answer.type });
      
      try {
        if (peerConnection.current) {
          await peerConnection.current.setRemoteDescription(answer);
          logMobileDebug('RÃ©ponse appliquÃ©e avec succÃ¨s');
          
          socket.emit('webrtc_answer', {
            type: 'answer_received',
            answerType: answer.type,
            debug: 'MOBILE_ANSWER_RECEIVED'
          });
        }
      } catch (error) {
        logMobileDebug('Erreur traitement rÃ©ponse', {
          error: error.message,
          name: error.name
        });
        
        socket.emit('webrtc_error', {
          type: 'answer_handling_error',
          error: error.message,
          debug: 'MOBILE_ANSWER_ERROR'
        });
      }
    };
    
    const handleIceCandidate = async (candidate) => {
      logMobileDebug('Candidat ICE reÃ§u', { 
        type: candidate.type,
        candidate: candidate.candidate?.substring(0, 50) + '...'
      });
      
      try {
        if (peerConnection.current) {
          await peerConnection.current.addIceCandidate(candidate);
          logMobileDebug('Candidat ICE ajoutÃ©');
          
          socket.emit('webrtc_ice', {
            type: 'ice_candidate_received',
            candidateType: candidate.type,
            debug: 'MOBILE_ICE_CANDIDATE_RECEIVED'
          });
        }
      } catch (error) {
        logMobileDebug('Erreur ajout candidat ICE', {
          error: error.message,
          name: error.name
        });
      }
    };
    
    socket.on('offer', handleOffer);
    socket.on('answer', handleAnswer);
    socket.on('ice-candidate', handleIceCandidate);
    
    return () => {
      socket.off('offer', handleOffer);
      socket.off('answer', handleAnswer);
      socket.off('ice-candidate', handleIceCandidate);
    };
  }, [socket, createPeerConnection, logMobileDebug]);

  // Nettoyage avec logs
  const cleanup = useCallback(() => {
    logMobileDebug('Nettoyage WebRTC');
    
    if (peerConnection.current) {
      peerConnection.current.close();
      peerConnection.current = null;
    }
    
    if (localStream) {
      localStream.getTracks().forEach(track => track.stop());
      setLocalStream(null);
    }
    
    setRemoteStream(null);
    setIsConnected(false);
    setConnectionState('new');
  }, [localStream, logMobileDebug]);

  // Toggle mute avec logs
  const toggleMute = useCallback(() => {
    if (localStream) {
      const audioTrack = localStream.getAudioTracks()[0];
      if (audioTrack) {
        audioTrack.enabled = !audioTrack.enabled;
        setIsMuted(!audioTrack.enabled);
        
        logMobileDebug('Mute togglÃ©', { 
          muted: !audioTrack.enabled,
          trackEnabled: audioTrack.enabled
        });
        
        if (socket) {
          socket.emit('webrtc_audio_state', {
            type: 'mute_toggle',
            enabled: audioTrack.enabled,
            muted: !audioTrack.enabled,
            debug: 'MOBILE_MUTE_TOGGLE'
          });
        }
      }
    }
  }, [localStream, socket, logMobileDebug]);

  // Log initial au chargement
  useEffect(() => {
    logMobileDebug('Hook WebRTC initialisÃ©', {
      hasSocket: !!socket,
      userAgent: navigator.userAgent.substring(0, 100)
    });
  }, [logMobileDebug, socket]);

  return {
    isConnected,
    remoteStream,
    localStream,
    isMuted,
    connectionState,
    startCall,
    startRemoteAudio,
    cleanup,
    toggleMute
  };
};

